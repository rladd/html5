<h2>Why Should You Care?<h2>
<h3>The end user experience.</h3>

<p>Welcome to the new world.</p>
<p>Although you might now realize it, by the time that you are reading this you are probably already using a fully compliant <b>HTML5</b> web browser!</p>
<p>At the time of this article it is estimated that at least <b>half</b> of all internet users are already using browsers which are ready for HTML5. Of the most popular, Firefox (3.5 and above), Google Chrome, Mac Safari, and Opera are the leading contenders. Internet Explorer will be catching up shortly with the next major release of IE.
</p>

<p>In the last few years we have witnessed computer usage expand well beyond your average student, programmer or aficiando. The community is now demanding better, faster, open source solutions to maximize their experience. Products such as <i>&copy; Adobe Flash</i> hinder our ability to improve and grow. Enter the ring - <b>HTML 5</b>.</p>

